    <h2 class="major">Power In The Field</h2>
    <span class="image main"><img src="images/charging.gif" alt="" /></span>
    <p>
        Power In The Field installiert Usb-Ladestationen an verschiedenen Orten in Luxemburg.
        Über die Webseite können Benutzer sich dann registrieren, anmelden und gegen Bezahlung von PITF (Währung) diese Ladestationen zum Laden ihrer Mobilgeräte benutzen.
    </p>
    <p>
        Unsere Ladestationen sind Handgefertigt und bestehen aus folgender Hardware: Raspberry Pi 3, LCD Display, LED's und einer Platine zur Steuerung der LED's.
        Nachdem Sie sich registriert haben, bekommen Sie ein Willkommens Guthaben vun 50PITF auf Ihr Konto. Um Ihr Guthaben wieder aufzuladen, gibt es 2 Möglichkeiten,
        Sie können entweder mit PayPal PITF kaufen oder PITF generieren indem Sie sich einloggen mit Ihrem Konto und dann werden Ihnen 0,25 PITF/min gutgeschrieben.
    </p>
